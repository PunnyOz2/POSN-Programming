#include<bits/stdc++.h>
using namespace std;
int dp[110][110][110][2][2][2];
int main(){
    int q,i,j,k;
    dp[1][0][0][1][0][0]=1;
    dp[0][1][0][0][1][0]=1;
    dp[0][0][1][0][0][1]=1;
    for(i=0;i<=100;i++){
        for(j=0;j<=100;j++){
            for(k=0;k<=100;k++){
                if(i+j+k<2)continue;
                if(i>0)dp[i][j][k][1][0][0]+=dp[i-1][j][k][0][1][0]+dp[i-1][j][k][0][0][1];
                if(j>0)dp[i][j][k][0][1][0]+=dp[i][j-1][k][1][0][0]+dp[i][j-1][k][0][0][1];
                if(k>0)dp[i][j][k][0][0][1]+=dp[i][j][k-1][1][0][0]+dp[i][j][k-1][0][1][0];
                dp[i][j][k][1][0][0]%=100003;
                dp[i][j][k][0][1][0]%=100003;
                dp[i][j][k][0][0][1]%=100003;
            }
        }
    }
    scanf("%d",&q);
    while(q--){
        scanf("%d %d %d",&i,&j,&k);
        printf("%d\n",(dp[i][j][k][1][0][0]+dp[i][j][k][0][1][0]+dp[i][j][k][0][0][1])%100003);
    }
    return 0;
}
